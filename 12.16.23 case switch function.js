function caseInSwitch(val) {
    let answer = "";
    switch (val) {
      case 1:
        answer = "Snow Leopard";
        break;
      case 2:
        answer = "Red Panda";
        break;
      case 3:
        answer = "Girraffe";
        break;
      case 4:
        answer = "Cat";
        break;
    }
    return answer;
  }
